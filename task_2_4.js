"use strict";

/**
 * Функция сложения 2-х чисел.
 * @param {number} a
 * @param {number} b
 * @returns {number} 
 */
function summ(a, b) {
    return a + b;
}

/**
 * Функция из параметра "а" вычитает параметр "b".
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function subtraction(a, b) {
    return a - b;
}

/**
 * Функция делит параметр "a" на параметр "b".
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function division(a, b) {
    return a / b;
}

/**
 * Функция умножает параметры.
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function multiplication(a, b) {
    return a * b;
}